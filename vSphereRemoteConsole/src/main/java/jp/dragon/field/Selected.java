package jp.dragon.field;

public class Selected {
	private String host_ = null;
	private String vm_ = null;
	
	public Selected() {
		
	}

	public String getHost() {
		return host_;
	}

	public void setHost(String host) {
		host_ = host;
	}

	public String getVm() {
		return vm_;
	}

	public void setVm(String vm) {
		vm_ = vm;
	}


}
